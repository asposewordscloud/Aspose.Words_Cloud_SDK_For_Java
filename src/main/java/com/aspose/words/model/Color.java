package com.aspose.words.model;

public class Color {
        private String Web = null;
        private Integer Alpha = null;

        /**
         * getWeb
         * Gets String
         * @return Web
         */
        public String getWeb() {
                return Web;
        }

        /**
         * setWeb
         * Sets String
         * @param Web String
         */
        public void setWeb(String web) {
                Web = web;
        }

        /**
         * getAlpha
         * Gets Integer
         * @return Alpha
         */
        public Integer getAlpha() {
                return Alpha;
        }

        /**
         * setAlpha
         * Sets Integer
         * @param Alpha Integer
         */
        public void setAlpha(Integer alpha) {
                Alpha = alpha;
        }

}
